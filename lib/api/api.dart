import 'dart:convert';

import 'package:submission_flutter/constant.dart';
import 'package:submission_flutter/models/movie.dart';
import 'package:http/http.dart' as http;

import '../models/detail_movie.dart';


class Api {
  static const _trendingUrl =
      'https://api.themoviedb.org/3/trending/movie/day?api_key=${Constants.apiKey}';
  static const _topRatedUrl =
      'https://api.themoviedb.org/3/movie/top_rated?api_key=${Constants.apiKey}';
  static const _upcomingUrl =
      'https://api.themoviedb.org/3/movie/upcoming?api_key=${Constants.apiKey}';

  Future<List<Movie>> getTrendingMovies() async {
    final response = await http.get(Uri.parse(_trendingUrl));
    if (response.statusCode == 200) {
      final decodedData = json.decode(response.body)['results'] as List;
      return decodedData.map((movie) => Movie.fromJson(movie)).toList();
    } else {
      throw Exception('Something happened');
    }
  }

  Future<List<Movie>> getTopRatedMovies() async {
    final response = await http.get(Uri.parse(_topRatedUrl));
    if (response.statusCode == 200) {
      final decodedData = json.decode(response.body)['results'] as List;
      return decodedData.map((movie) => Movie.fromJson(movie)).toList();
    } else {
      throw Exception('Something happened');
    }
  }

  Future<List<Movie>> getUpcomingMovies() async {
    final response = await http.get(Uri.parse(_upcomingUrl));
    if (response.statusCode == 200) {
      final decodedData = json.decode(response.body)['results'] as List;
      return decodedData.map((movie) => Movie.fromJson(movie)).toList();
    } else {
      throw Exception('Something happened');
    }
  }

  Future<DetailMovie> getDetailMovie(int movieID) async {
    final detailMovieUrl =
        'https://api.themoviedb.org/3/movie/$movieID?api_key=${Constants.apiKey}';
    final response = await http.get(Uri.parse(detailMovieUrl));
    if (response.statusCode == 200) {
      return DetailMovie.fromJson(jsonDecode(response.body) as Map<String, dynamic>);
    } else {
      throw Exception('Something happened');
    }
  }
}